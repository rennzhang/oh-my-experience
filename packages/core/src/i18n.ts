type Locale = keyof typeof MESSAGES;
type MessageKey = keyof typeof MESSAGES.en;

export function normalizeLocale(locale = "en") {
  return String(locale || "en").toLowerCase().includes("zh") ? "zh-CN" : "en";
}

export function uiDictionary(locale = "en") {
  const normalized = normalizeLocale(locale) as Locale;
  return { ...MESSAGES.en, ...(MESSAGES[normalized] || {}) };
}

export function t(key: string, locale = "en") {
  return uiDictionary(locale)[key as MessageKey] || key;
}
